/*Задание 3
Используя Math.random() вам необходимо генерировать цифры от 0 до 9, и создать массив состоящий из 5 таких элементов
1. Рассчитать сумму элементов этого массива
2. Найти минимальное число
3. Найти есть ли в этом массиве число 3 */
//------------------------------------------------------------------//

const arrayRandom = [];
for (let i = 0; i < 5; i++) {
  arrayRandom.push(Math.floor(Math.random() * (10 - 0)) + 0);
}

console.log(arrayRandom);

let minInArray = Math.min(...arrayRandom);
console.log(`Минимальное число = ${minInArray}`);

let numbsThreeInArray = arrayRandom.filter((element) => element === 3).length;
console.log(`Количество 3 в массиве = ${numbsThreeInArray}`);

