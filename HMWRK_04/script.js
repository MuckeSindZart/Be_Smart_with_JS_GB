/* Задание 1
Необходимо с помощью цикла for вывести следующие 11 строк в консоль:
0 – это ноль
1 – нечетное число
2 – четное число
3 – нечетное число
…
10 – четное число */
//------------------------------------------------------------------//

for (let i = 0; i <= 10; i++) {
  switch (true) {
    case i === 0:
      console.log(`${i} – это ноль`);
      continue;
    case i % 2 === 0:
      console.log(`${i} – четное число`);
      continue;
    default:
      console.log(`${i} – нечетное число`);
      continue;
  }
}

